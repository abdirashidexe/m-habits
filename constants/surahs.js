/**
 * @param {string} query
 * @returns {{ index: number, en: string, ar: string }[]}
 */
export function searchSurahs(query) {
  const q = query.trim().toLowerCase();
  if (!q) return [];
  const out = [];
  for (let i = 0; i < SURAH_NAMES_EN.length; i++) {
    const en = SURAH_NAMES_EN[i];
    const ar = SURAH_NAMES_AR[i];
    if (en.toLowerCase().includes(q) || ar.includes(query.trim())) {
      out.push({ index: i + 1, en, ar });
    }
    if (out.length >= 12) break;
  }
  return out;
}
